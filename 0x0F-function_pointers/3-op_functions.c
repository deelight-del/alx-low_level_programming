#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
*op_add - function to add two integers
*@a: first operand
*@b: second operand
*
*Return: integer value of the result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - function to subtract two integers
*@a: first operand
*@b: second operand
*
*Return: integer value of the result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - function to multiply two integers
*@a: first operand
*@b: second operand
*
*Return: integer value of the result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div - function to add two integers
*@a: first operand
*@b: second operand
*
*Return: integer value of the result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - function to add two integers
*@a: first operand
*@b: second operand
*
*Return: integer value of the result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
